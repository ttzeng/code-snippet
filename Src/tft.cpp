tft::tft(uint16_t width, uint16_t height)
{
    display_width = width, display_height = height;
    default_font = &Font7x10;
}

tft& tft::line(int16_t x1, int16_t y1, int16_t x2, int16_t y2, uint16_t color)
{
    int dx = abs((int)(x2 - x1));
    int dy = abs((int)(y2 - y1));
    int sx = x1 < x2 ? 1 : -1;
    int sy = y1 < y2 ? 1 : -1;
    int err = dx - dy;

    while (1) {
        pixel(x1, y1, color);
        if (x1 == x2 && y1 == y2) break;
        int e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y1 += sy;
        }
    }
    return *this;
}

tft& tft::rect(int16_t x, int16_t y, uint16_t width, uint16_t height, uint16_t color)
{
    line(x, y, x + width - 1, y, color);                           // Top
    line(x, y, x, y + height - 1, color);                          // Left
    line(x + width - 1, y, x + width - 1, y + height - 1, color);  // Right
    line(x, y + height - 1, x + width - 1, y + height - 1, color); // Bottom
    return *this;
}

tft& tft::fill(int16_t x, int16_t y, uint16_t width, uint16_t height, uint16_t color)
{
    for (uint16_t i = 0; i < height; i++)
        for (uint16_t j = 0; j < width; j++)
            pixel(x + j, y + i, color);
    return *this;
}

tft& tft::clear(uint16_t color)
{
    fill(0, 0, display_width, display_height, color);
    return *this;
}

tft& tft::circle(int16_t x0, int16_t y0, int16_t r, uint16_t color)
{
    int16_t x = r;
    int16_t y = 0;
    int16_t err = 0;

    while(x >= y) {
        pixel(x0 + x, y0 + y, color);
        pixel(x0 + y, y0 + x, color);
        pixel(x0 - y, y0 + x, color);
        pixel(x0 - x, y0 + y, color);
        pixel(x0 - x, y0 - y, color);
        pixel(x0 - y, y0 - x, color);
        pixel(x0 + y, y0 - x, color);
        pixel(x0 + x, y0 - y, color);
        if (err <= 0) {
            y += 1;
            err += 2 * y + 1;
        }
        if (err > 0) {
            x -= 1;
            err -= 2 * x + 1;
        }
    }
    return *this;
}

tft& tft::fill(int16_t x0, int16_t y0, int16_t r, uint16_t color)
{
    int16_t x = r;
    int16_t y = 0;
    int16_t err = 0;

    while (x >= y) {
        line(x0 - x, y0 + y, x0 + x, y0 + y, color);
        line(x0 - y, y0 + x, x0 + y, y0 + x, color);
        line(x0 - x, y0 - y, x0 + x, y0 - y, color);
        line(x0 - y, y0 - x, x0 + y, y0 - x, color);
        if (err <= 0) {
            y += 1;
            err += 2 * y + 1;
        }
        if (err > 0) {
            x -= 1;
            err -= 2 * x + 1;
        }
    }
    return *this;
}

tft& tft::draw_char(int16_t x, int16_t y, char ch, uint16_t color, uint16_t bgcolor, FontDef* font)
{
    if (font) default_font = font;

    uint32_t i, b, j;

    for (i = 0; i < default_font->height; i++) {
        b = default_font->data[(ch - 32) * default_font->height + i];
        for (j = 0; j < default_font->width; j++)
            pixel(x + j, y + i, ((b << j) & 0x8000)? color : bgcolor);
    }
    return *this;
}

tft& tft::draw_string(int16_t x, int16_t y, const char* str, uint16_t color, uint16_t bgcolor, FontDef* font)
{
    uint8_t font_width =  font? font->width  : default_font->width;
    uint8_t font_height = font? font->height : default_font->height;

    for (; *str; str++) {
        if (x + font_width >= display_width) {
            x = 0, y += font_height;
            if (y + font_height >= display_height)
                break;
        }
        draw_char(x, y, *str, color, bgcolor, font);
        x += font_width;
    }
    return *this;
}
